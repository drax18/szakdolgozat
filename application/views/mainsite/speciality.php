Az ír kávé írsége
 

Először is szükségünk lesz egy speciális, ír-kávés pohárra, mely hőálló, talpas, füles üvegpohár. Formája, öblössége leginkább egy kis söröskorsóhoz hasonlítható. Ebbe a pohárba 4cl ír whiskey-t, és ízlés szerinti (2-3 teáskanál) barnacukrot öntünk. Ezután két dolgot tehetünk. Meggyújtjuk az italt és a cukrot folyamatosan kevergetve karamellizáljuk, vagy a kávégép gőzölőjével addig melegítjük, amíg teljesen fel nem oldjuk a cukrot. Ezután felengedjük kávéval addig a pontig, ahol a pohár íve elkezd kiszélesedni. Shakerben tejszínt habosítunk, de csak annyira, hogy még csurgatni lehessen. Ezt egy, a kávé felszínén tartott kanálra öntjük, így koronázva meg habbal az italt. A jól elkészített ír kávé megjelenésében, karamelles-kesernyés ízében és hatásában is emlékeztet az írek leghíresebb sörére. A fogyasztás is hasonlóan történik: a kávé íze és jellege úgy érvényesül leginkább, ha a habon keresztül isszuk ki, éppen úgy, mintha egy hűs Guinesst kortyolgatnánk.

Limoncello Hot Shot




Az 5cl-es shotos pohár falát belül és a peremén citromhéjjal bekenjük. Így felszabadítjuk a citromolaj aromáit. A pohár aljába 2cl limoncellót (olasz citromlikőrt) öntönk. Erre rétegezzük óvatosan, kanál segítségével a forró espressót (2cl). Hideg tejszínbe finomra reszelt citromhéjat teszünk, ezt három részes shakerben krémesre habosítjuk. A tejszínt a kávé fölé tartott kanálra csurgatva rétegezzük az ital tetejére. Rögtön szervírozzuk, egy hajtásra fogyasztjuk.

Angyalok csókja




Egy max. 1,5 dl űrtartalmú margaritás (talpas, ipszilon alakú) pohár aljára 2 cl kávélikőr töltünk, melyre bárkanál segítségével 2cl ír krémlikőrt rétegezünk. A kávégép gőzölőjével erős tejhabot készítünk (kb. 6cl), melyet jól összedolgozunk és hasonlóképpen rétegezve pohárba töltünk. Presszókávét készítünk (2-3cl), amit a kiöntőből a tejhab közepére csurgatunk. A tejhab tetején keletkező kávéfoltot a maradék tejhabbal eltüntetjük. A jól elkészített ital 6 rétegben pompázik.

